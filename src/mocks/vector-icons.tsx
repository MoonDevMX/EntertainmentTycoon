interface IconProps {
  name: string;
  size?: number;
  color?: string;
  style?: any;
}

export function MaterialCommunityIcons({ name, size = 24, color = 'white', style }: IconProps) {
  // Normalize key to standard lucide tag name
  const mappedName = iconMap[name] || 'HelpCircle';
  // Retrieve component dynamically
  const LucideComponent = (Lucide as any)[mappedName] || Lucide.HelpCircle;

  return (
    <div style={{ display: 'inline-flex', alignItems: 'center', justifyContent: 'center', ...style }}>
      <LucideComponent size={size} color={color} />
    </div>
  );
}
